# Write the python program for assigning the different date type value in a variable and display the data type name using type().

variable = 108
print(type(variable))   # <class 'int'>

variable = 108.80
print(type(variable))   # <class 'float'>

variable = 3 + 2j
print(type(variable))   # <class 'complex'>

variable = True
print(type(variable))   # <class 'bool'>

variable = "We are learning Python"
print(type(variable))   # <class 'str'>
