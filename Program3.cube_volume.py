# Write a python Program to calculate and display the volume of a CUBE having its height (h=10cm), width (w=12cm) and depth (8cm). 

# Given values
height = 10  # in centimeters
width = 12   # in centimeters
depth = 8    # in centimeters

# Calculating the volume of the cube
volume = height * width * depth

# Displaying the result
print(f"The volume of the cube is: {volume} cubic centimeters")
