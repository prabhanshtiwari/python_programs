# Write a program to demonstrate the use of ord() and chr().

print("Using ord() --> to get ASCII values")
print("ASCII value of Lowercase letters")
print("a =", ord("a")) 
print("b =", ord("b"))

print("ASCII value of Uppercase letters")
print("A =", ord("A"))
print("B =", ord("B"))

print("ASCII value of Special symbols")
print("! =", ord("!"))
print("@ =", ord("@"))


print("Using chr() --> to get character of the given ASCII values")
print(97, "=", chr(97))
print(98, "=", chr(98))
